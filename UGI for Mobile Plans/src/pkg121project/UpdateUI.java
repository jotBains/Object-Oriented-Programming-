    public UpdateUI(Plan plan, userGUI UserGUI) {
        initComponents();
        this.UserGUI = UserGUI;
        this.plan = plan;
        planIDTextField.setText(plan.getID() + "");
        newUserNameTextField.setText(plan.getUserName());
        modelTextField.setText(plan.getModel());
        typeComboBox.setModel(new DefaultComboBoxModel<>(MobileType.values()));
        MemorySizeTextField.setText(plan.getHandset().getSize() + "");
        priceTextField.setText(plan.getHandset().getPrice() + "");
        InternetQuotaTextField.setText(plan.getInternetQuota() + "");
        CapLimitTextField.setText(plan.getCapLimit() + "");
        dayTextField.setText(plan.getExpiryDate().getDay() + "");
        monthTextField.setText(plan.getExpiryDate().getMonth() + "");
        yearTextField.setText(plan.getExpiryDate().getYear() + "");
        if (plan instanceof PersonalPlan) {
            choicePersonalPlan.setSelected(true);
            CityAndABNLabel.setText("City");
            cityAndABNTextField.setText(((PersonalPlan) plan).city);
            NumberOfEmpText.setText("");
            EmployessNumberTextField.setVisible(false);
        } else {
            choiceBusinessPlan.setSelected(true);
            CityAndABNLabel.setText("ABN");
            cityAndABNTextField.setText(((BusinessPlan) plan).ABN + "");
            EmployessNumberTextField.setText(((BusinessPlan) plan).numberOfEmployess + "");
            EmployessNumberTextField.setVisible(true);
            NumberOfEmpText.setVisible(true);
        }
        choicePersonalPlan.setEnabled(false);
        choiceBusinessPlan.setEnabled(false);
        planIDTextField.setEditable(false);
    }

