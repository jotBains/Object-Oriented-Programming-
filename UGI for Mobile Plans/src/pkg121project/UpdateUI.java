    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        try {
            String userName = newUserNameTextField.getText();
            String model = modelTextField.getText();
            MobileType type = MobileType.valueOf(typeComboBox.getSelectedItem().toString());
            int size = Integer.parseInt(MemorySizeTextField.getText());
            double price = Double.parseDouble(priceTextField.getText());
            int InternetQuota = Integer.parseInt(InternetQuotaTextField.getText());
            int CapLimit = Integer.parseInt(CapLimitTextField.getText());
            int day = Integer.parseInt(dayTextField.getText());
            int month = Integer.parseInt(monthTextField.getText());
            int year = Integer.parseInt(yearTextField.getText());
            MyDate date = new MyDate(day, month, year);
            MobilePhone phone = new MobilePhone(model, type, size, price);
            plan.setUserName(userName);
            plan.setMoible(phone);
            plan.setDate(date);
            plan.setInternetQuota(InternetQuota);
            plan.setCapLimit(CapLimit);
            if (choicePersonalPlan.isSelected()) {
                String City = cityAndABNTextField.getText();
                ((PersonalPlan) plan).city = City;
            } else {
                int abn = Integer.parseInt(cityAndABNTextField.getText());
                int nOEmop = Integer.parseInt(EmployessNumberTextField.getText());
                ((BusinessPlan) plan).ABN = abn;
                ((BusinessPlan) plan).numberOfEmployess = nOEmop;
            }
            JOptionPane.showMessageDialog(this, "Plan has been updated");
            UserGUI.fillTable();
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_UpdateActionPerformed

